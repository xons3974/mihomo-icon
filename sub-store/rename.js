function operator(pro) {
  pro.forEach((e) => {
    let name = e.name;
    let matchedCountry = null;

    // 1. 尝试特殊映射预处理（如 SIN -> SG）
    for (let key in extraMap) {
      if (new RegExp("\\b" + key + "\\b", "i").test(name)) {
        name = name.replace(new RegExp(key, "gi"), extraMap[key]);
      }
    }

    // 2. 核心匹配查找
    // 优先匹配两字代码（带词边界），再匹配中文/长英文
    matchedCountry = UnifiedData.find(item => {
      return new RegExp("\\b" + item.code + "\\b", "i").test(name) || 
             name.includes(item.keywords[1]) || // ZH
             name.includes(item.keywords[2]);   // QC
    });

    if (matchedCountry) {
      // 3. 识别标签和倍率
      let ikey = "", ikeys = "";
      regexArray.forEach((r, i) => { if (r.test(e.name)) ikeys = valueArray[i]; });
      if (bl) {
        const m = e.name.match(/((倍率|X|x|×)\D?((\d{1,3}\.)?\d+)\D?)|((\d{1,3}\.)?\d+)(倍|X|x|×)/);
        if (m) {
          const rev = m[0].match(/(\d[\d.]*)/)[0];
          if (rev !== "1") ikey = rev + "×";
        }
      }

      // 4. 组装结果
      let finalParts = [];
      if (addflag) finalParts.push(matchedCountry.flag);
      if (FNAME) finalParts.push(FNAME);
      finalParts.push(matchedCountry.code);
      if (ikeys) finalParts.push(ikeys);
      if (ikey) finalParts.push(ikey);

      e.name = finalParts.filter(p => p !== "").join(FGF);
    } else {
      // 没匹配上的节点：如果有 nm 参数则保留原名，否则删除
      if (!nm) e.name = null;
    }
  });

  // 清洗列表并去重
  pro = pro.filter(e => e.name !== null);
  jxh(pro);
  if (numone) oneP(pro);
  if (blpx) pro.sort((a, b) => a.name.localeCompare(b.name));
  
  return pro;
}

